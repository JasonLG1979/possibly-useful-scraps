from gi.repository import GLib

def GLib_idle(priority=GLib.PRIORITY_DEFAULT_IDLE):
    def wrapper(f):
        def run(*args, **kwargs):
            GLib.idle_add(lambda: f(*args, **kwargs), priority=priority)
        return run
    return wrapper
